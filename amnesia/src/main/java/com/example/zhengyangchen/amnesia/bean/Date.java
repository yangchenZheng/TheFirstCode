package com.example.zhengyangchen.amnesia.bean;

/**
 * 时间实例
 * Created by zhengyangchen on 2015/11/4.
 */
public class Date {
    private int year;
    private int month;
    private String monthAndTime;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public String getMonthAndTime() {
        return monthAndTime;
    }

    public void setMonthAndTime(String monthAndTime) {
        this.monthAndTime = monthAndTime;
    }
}
